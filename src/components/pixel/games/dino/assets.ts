function bitmapToSprite(rows: readonly string[], width: number): Sprite {
  return rows.map((row) =>
    row
      .slice(0, width)
      .padEnd(width, "0")
      .replaceAll("0", " ")
      .replaceAll("1", "#"),
  );
}
