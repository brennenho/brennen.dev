function bitmapToSprite(rows: readonly string[], width: number): Sprite {
  return rows.map((row) =>
    row
      .slice(0, width)
      .padEnd(width, "0")
      .replaceAll("0", " ")
      .replaceAll("1", "#"),
  );
}

function cactusVariant(
  sprite: Sprite,
  type: CactusType,
  chromeBaseWidth: number,
  chromeSize: 1 | 2 | 3,
  multipleSpeed: number,
): CactusVariant {
  return {
    chromeBaseWidth,
    chromeSize,
    multipleSpeed,
    sprite,
    type,
  };
}
